package de.hdm_stuttgart.se2_projekt.pascalWiss.data;

/**
 * Simple Calculations used for the Class EnemyMovement
 *
 */
public class SimpleCalc {

	/**
	 * Adds two
	 * @param x
	 * @return x+2
	 */
	public static int AddTwo(int x) {
		return x += 2;
	}

	/**
	 * Remove two
	 * @param x
	 * @return x-2
	 */
	public static int RmTwo(int x) {
		return x -= 2;
	}

	/**
	 * Divide with two
	 * @param x
	 * @return 1/2 x
	 */
	public static int Devi2(int x) {
		return x = x / 2;
	}

	

}
